static void put_pixel(draw_context_t *ctx, int x, int y, uint32_t color) {
  if (x < 0 || y < 0 || x >= ctx->info.width || y >= ctx->info.height) return;
  uint8_t *px = ctx->fb + (uint32_t)y * ctx->info.pitch + (uint32_t)x * ctx->info.bytes_per_pixel;
  px[0] = (uint8_t)(color & 0xFFu);
  px[1] = (uint8_t)((color >> 8) & 0xFFu);
  px[2] = (uint8_t)((color >> 16) & 0xFFu);
}

uint32_t font_text_width(const char *text, uint8_t scale) {
  if (scale == 0) scale = 1;
  size_t len = 0;
  while (text[len] != 0) len++;
  if (len == 0) return 0;
  return (uint32_t)((len * 6u - 1u) * scale);
}

void draw_text(draw_context_t *ctx, int x, int y, const char *text, uint32_t color, uint8_t scale) {
  if (scale == 0) scale = 1;
  for (size_t i = 0; text[i] != 0; i++) {
    const uint8_t *glyph = glyph_for(text[i]);
    int gx = x + (int)i * 6 * scale;
    for (int row = 0; row < 7; row++) {
      for (int col = 0; col < 5; col++) {
        if ((glyph[row] & (1u << (4 - col))) == 0) continue;
        for (uint8_t sy = 0; sy < scale; sy++) {
          for (uint8_t sx = 0; sx < scale; sx++) {
            put_pixel(ctx, gx + col * scale + sx, y + row * scale + sy, color);
          }
        }
      }
    }
  }
}
